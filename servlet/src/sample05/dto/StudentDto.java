package sample05.dto;

public class StudentDto {

    String name;

    int age;

    String address;

    public StudentDto(String name, int age, String address) {
        this.name = name;
        this.age = age;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getAddress() {
        return address;
    }
}
